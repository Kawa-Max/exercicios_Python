"""
desenvolva um programa que ira ler duas notas de um aluno
                    e
calcule, mostre a media entre eles
"""
nota1 = float(input('\033[35mNota 1: '))
nota2 = float(input('Nota 2: '))
media = (nota1 + nota2) / 2
print('\033[35mCom as notas acima, tem se a media: {}'.format(media))

